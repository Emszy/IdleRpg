function npcImage(srcs) {
	function directions(end) {
		return ({
					up : {
					index : 0,
					end : end,
					},
					left : {
						index : 1,
						end : end,
					},
					down : {
						index : 2,
						end : end,
					},
					right : {
						index : 3,
						end : end,
					}
				})
	}

	let imgObject = {
		eat:  {
			img : new Image(),
			directions : directions(3)
		},
		walk : {
			img : new Image(),
			directions : directions(3)
		},
	}

	imgObject.walk.img.src = srcs.walk
	imgObject.eat.img.src = srcs.eat
	return (imgObject)
}


